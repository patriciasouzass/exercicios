package fundamentos.operadores;

public class Aritmeticos {
    public static void main(String[] args) {

        System.out.println(2 + 3);

        var x = 32.45;
        double y = 7.5;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        int a = 8;
        int b = 3;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println((double)a / b);
        System.out.println(a % b);
    }
}
